static uint64_t fb_addr;
static uint32_t fb_width;
static uint32_t fb_height;
static uint32_t fb_pitch;
static uint8_t  fb_bpp;
static uint32_t fb_cols;
static uint32_t fb_rows;
static uint32_t cur_col;
static uint32_t cur_row;
static int      fb_ready;

static void put_pixel(uint32_t x, uint32_t y, uint32_t color)
{
    uint8_t *p = (uint8_t *)(uintptr_t)(fb_addr + y * fb_pitch + x * (fb_bpp / 8));
    switch (fb_bpp) {
    case 32:
        p[0] = color & 0xff;
        p[1] = (color >> 8) & 0xff;
        p[2] = (color >> 16) & 0xff;
        p[3] = 0;
        break;
    case 24:
        p[0] = color & 0xff;
        p[1] = (color >> 8) & 0xff;
        p[2] = (color >> 16) & 0xff;
        break;
    case 16: {
        uint16_t c = (uint16_t)(((color >> 19) & 0x1f) << 11 |
                                ((color >> 10) & 0x3f) << 5  |
                                ((color >>  3) & 0x1f));
        p[0] = c & 0xff;
        p[1] = (c >> 8) & 0xff;
        break;
    }
    }
}

static void draw_char(uint32_t col, uint32_t row, char c)
{
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7F)
        c = '?';
    const uint8_t *glyph = font[(unsigned char)c - 0x20];
    uint32_t px = col * FONT_W;
    uint32_t py = row * FONT_H;
    for (int gy = 0; gy < FONT_H; gy++) {
        uint8_t row_bits = glyph[gy];
        for (int gx = 0; gx < FONT_W; gx++) {
            uint32_t color = (row_bits & (1 << gx)) ? FG : BG;
            put_pixel(px + gx, py + gy, color);
        }
    }
}

static void scroll_up(void)
{
    uint32_t row_bytes = fb_pitch * FONT_H;
    uint8_t *dst = (uint8_t *)(uintptr_t)fb_addr;
    uint8_t *src = dst + row_bytes;
    uint32_t copy_bytes = row_bytes * (fb_rows - 1);

    for (uint32_t i = 0; i < copy_bytes; i++)
        dst[i] = src[i];

    uint8_t *last_row = dst + copy_bytes;
    for (uint32_t i = 0; i < row_bytes; i++)
        last_row[i] = 0;
}

void fb_init(uint64_t addr, uint32_t width, uint32_t height,
             uint32_t pitch, uint8_t bpp)
{
    fb_addr   = addr;
    fb_width  = width;
    fb_height = height;
    fb_pitch  = pitch;
    fb_bpp    = bpp;
    fb_cols   = width  / FONT_W;
    fb_rows   = height / FONT_H;
    cur_col   = 0;
    cur_row   = 0;
    fb_ready  = 1;

    /* Clear screen */
    uint8_t *p = (uint8_t *)(uintptr_t)addr;
    for (uint32_t i = 0; i < height * pitch; i++)
        p[i] = 0;
}

int fb_active(void) { return fb_ready; }

void fb_putc(char c)
{
    if (!fb_ready)
        return;

    if (c == '\n') {
        cur_col = 0;
        cur_row++;
    } else if (c == '\r') {
        cur_col = 0;
    } else if (c == '\t') {
        cur_col = (cur_col + 8) & ~7u;
    } else if (c == '\b') {
        if (cur_col > 0)
            cur_col--;
    } else {
        draw_char(cur_col, cur_row, c);
        cur_col++;
        if (cur_col >= fb_cols) {
            cur_col = 0;
            cur_row++;
        }
    }

    if (cur_row >= fb_rows) {
        scroll_up();
        cur_row = fb_rows - 1;
    }
}
